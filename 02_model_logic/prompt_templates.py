# ── 2. 메인 추천 질문 템플릿 ────────────────────────────────────────────────

def get_main_recommendation_prompt(query: str, ctx: str) -> str:
    # Recency Bias 적용: Context를 먼저 주고, 사용자 질문을 마지막에 배치
    return (
        f"[참고 데이터 (Context)]\n{ctx}\n\n"
        f"위 데이터를 바탕으로 다음 사용자의 질문에 답변해줘. 반드시 [절대 규칙]을 지켜야 해.\n\n"
        f"[사용자 질문]\n**{query}**"
    )

# ── 3. 개별 장소 카드 추천 템플릿 ─────────────────────────────────────────────
def get_card_recommendation_prompt(
    name: str,
    addr: str,
    feats: list,
    review: str,
    age_sel: str = "",
) -> str:
    feat_str = ", ".join(feats[:5]) or "다양한 놀이 시설"
    age_line = f"아이 연령: {age_sel}\n" if age_sel else ""

    return (
        f"장소: {name} ({addr})\n"
        f"{age_line}특징: {feat_str}\n"
        f"리뷰: {review[:300]}\n\n"
        "[지시사항]\n"
        "- 이 장소의 핵심 장점 1~2개를 먼저 설명하세요\n"
        "- 리뷰를 기반으로 실제 방문 경험을 구체적으로 표현하세요\n"
        "- 아이가 실제로 어떻게 놀지 상상할 수 있게 작성하세요\n"
        "- 마지막에 '💡 이모삼촌의 꿀팁:'에는 이 장소만의 현실적인 팁을 제공하세요\n"
        "- 절대 뻔한 말(주말 혼잡, 편한 복장 등)은 금지합니다\n"
    )